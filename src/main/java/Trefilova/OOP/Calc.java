package Trefilova.OOP;

public class Calc {

    public double sumTwo (double a, double b) {
        return a + b;
    }

    public double sumThree(double a, double b, double c) {
        return a + b + c;
    }

    public double division (double a, double b) {
        return a / b;
    }

    public double multiplication (double a) {
        return a * a;
    }
}
