package Gaisenok;

public class Calc {

    double addNumbers(double numberOne, double numberTwo){
        return numberOne+numberTwo;
    }
    double addNumbers(double numberOne, double numberTwo, double numberThree){
        return numberOne+numberTwo+numberThree;
    }
    double numbersDivision(double numberOne, double numberTwo){
        return numberOne / numberTwo;
    }
    double numberMultiplyItself(double number){
        return number*number;
    }
}
