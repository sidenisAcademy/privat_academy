package Nakhanov.OOP;

public class DoCalc {

    int number;

    public static int powerCalc(int x){
        return x * x;
    }
    public static int sumCalc(int x, int y){
        return x + y;
    }
    public static int sumCalc(int x, int y, int z){
        return x + y + z;
    }
    public static double divCalc(double x, double y){
        return x/y;
    }

}
